<script>
import Watcher from './Watcher'
export default {
  props: ['data'],
  components: { Watcher },
  data () {
    return {
      autostart: false,
      startFrom: 0,
      // animation steps in deg
      steps: [0, 31.2, 65.8, 90, 128.2, 167.8, 180, 252.2, 285.2],
      // animation interval
      animation: null,
      // animation status
      running: false,
      // spinning rings reference
      rings: {
        bbox: undefined
      },
      // process items list
      process: undefined,
      // current step
      step: 7,
      // loop itearation
      loop: 0,
      // canvas
      paper: undefined,
      waiting: undefined,
      speed: {
        // step animation duration
        step: 750,
        // time we stay on a step
        interval: 1500
      }
    }
  },
  mounted () {
    this.paper = Snap("#svgmap")
    this.init()
  },
  watch: {
    // watchers is the only one in charge of firing animation and checking boundaries
    step (nv, ov) {
      if (nv >= this.steps.length) {
        this.loop++
        this.step = 0
      }
      if (nv < 0) {
        this.loop--
        this.step = this.steps.length - 1
      }
      this.goto(this.step, ov)
    }
  },
  methods: {
    init () {
      // setup selectors
      this.rings = this.paper.select('#rings')
      this.rings.bbox = this.rings.getBBox()
      // process steps
      this.process = this.paper.select('#process')
      // move to preselected step
      this.step = this.startFrom
      if (this.autostart) {
        this.start()
      }
    },
    start () {
      if (this.running) {
        return
      }
      this.running = true
      // ---------- MAIN Animation Loop --------
      this.animation = setInterval(() => {
        this.step++
      }, this.speed.interval + this.speed.step)
    },
    goto (next, current) {
      this.gotoProcessStep(next, current)
      this.gotoRingsStep(next, current)
    },
    gotoProcessStep (next, current) {
      // check boundaries
      current = current >= this.steps.length - 1 ? this.steps.length - 1 : current
      current = current < 0 ? 0 : current
      // hide current
      this.process
        .select(`.step:nth-child(${current + 1})`)
        .stop()
        .animate({opacity: 0, transform: 't0, 100'}, this.speed.step / 2)
      // fade in new
      this.process
        .select(`.step:nth-child(${next + 1})`)
        .stop()
        .transform('t0, -100')
        .animate({opacity: 1, transform: 't0, 0'}, this.speed.step)
    },
    gotoRingsStep (next) {
      this.rings
        .stop()
        .animate({
          transform: `r${this.steps[next] + (360 * this.loop)} ${this.rings.bbox.cx} ${this.rings.bbox.cy}`
        }, this.speed.step, mina.easein)
    },
    stop () {
      clearInterval(this.animation)
      this.animation = null
      this.running = false
    },
    scroll (amt) {
      this.toStep(this.step + amt)
    },
    toStep (step) {
      clearTimeout(this.waiting)
      // stop animation
      this.stop()
      this.step = step
      // queue animation start
      this.waiting = setTimeout(() => {
        this.start()
      }, this.speed.interval)
    }
  }
}
